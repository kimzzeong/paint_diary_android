package com.example.paint_diary.Paint;

import android.graphics.MaskFilter;
import android.graphics.Path;

public class PathXY {

    private float x;
    private float y;
    public PathXY(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }


}
